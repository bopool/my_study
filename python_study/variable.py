# 변수 이름 규칙 
# 영어, 숫자, _ 만 사용 (파이썬은 한글도 됨)
# 제일 앞에는 숫자가 오면 안된다. 
# 키워드 사용 불가 : 변수로 정의할 수 없는 단어들이 있다. 

name = "본인 이름" # 변수값을 꼭 넣어줘야 한다. 
first_name = "하연"
last_name = "김" 
name = last_name + first_name #변수값은 재할당 가능하다. 최신값 반영됨. 
print(name)

a = 6 #변수를 정의하고 값을 할당해 준 것. 
b = 2
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a ** b)
print(a // b)
print(a % b)
